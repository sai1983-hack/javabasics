package simpleprogramofjava;

public class Operator {
public static void main(String[]args) {
    int x=60;
    int y=60;
    System.out.println("Logical Operator");
    System.out.println((x==y)&&(x>y));   
    System.out.println((x==y)||(x<y));
    System.out.println(!(x==y)&&(x<y));
}
}
